public class  Operadores3 {
    public static void main (String[] args){

            int n1= 1, n2 =2;
                   
//se a variável é IGUAL   ==
       boolean simNao= n1 ==n2;
            System.out.println("Número 1 é igual ao número 2? "+ simNao);

//Se a variável é DIFERENTE    !=
    simNao= n1!=n2;
            System.out.println("Número 1 é diferente ao número 2? "+ simNao);

//Se a variável é MAIOR QUE   >
    simNao= n1>n2;
        System.out.println("Número 1 é maior que o número 2? "+ simNao);

//Se a variável é MENOR QUE   <
    simNao= n1<n2;
        System.out.println("Número 1 é menor que o número 2? "+ simNao);

//Se a variável é MENOR ou IGUAL a <=
    simNao= n1<=n2;
        System.out.println("Número 1 é menor ou igual que o número 2? "+ simNao);

//Se a variável é MAIOR ou IGUAL a >=
    simNao= n1>=n2;
        System.out.println("Número 1 é maior ou igual que o número 2? "+ simNao);
    }
}